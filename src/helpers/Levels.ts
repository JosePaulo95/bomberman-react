import { Level } from "@/types"

export const createTerrainMap = (level: number): Level => {
    switch (level) {
        case 1:
            // Terreno misturado
            return {
                map: [
                    [0, 2, 1, 0, 0, 1, 2, 0],
                    [0, 1, 0, 1, 2, 0, 1, 0],
                    [0, 2, 2, 0, 1, 0, 0, 2],
                    [0, 1, 0, 0, 0, 2, 0, 1],
                    [0, 0, 0, 0, 0, 0, 2, 0],
                    [1, 0, 2, 0, 1, 0, 1, 2],
                    [0, 2, 1, 0, 0, 1, 2, 0],
                    [0, 1, 0, 1, 2, 0, 1, 0]
                ]
            }
        default:
            // Terreno padrÃ£o, todo 0
            return {
                map: [
                    [0, 2, 1, 0, 0, 1, 2, 0],
                    [0, 1, 0, 1, 2, 0, 1, 0],
                    [0, 2, 2, 0, 1, 0, 0, 2],
                    [0, 1, 0, 0, 0, 2, 0, 1],
                    [0, 0, 0, 0, 0, 0, 2, 0],
                    [1, 0, 2, 0, 1, 0, 1, 2],
                    [0, 2, 1, 0, 0, 1, 2, 0],
                    [0, 1, 0, 1, 2, 0, 1, 0]
                ]
            }
    }
}
